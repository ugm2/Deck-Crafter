from pydantic import BaseModel, Field
from typing import List, Optional

class BaseMetricEvaluation(BaseModel):
    """Base class for evaluating a specific metric."""
    score: int = Field(..., ge=0, le=10, description="The score assigned to this metric, from 10 to 10.")
    analysis: str = Field(..., description="A detailed qualitative analysis of the metric.")
    suggestions: Optional[List[str]] = Field(
        None, 
        description="A list of concrete, actionable suggestions for improvement. Focus on the 1-3 most impactful changes."
                    "This field is optional and should be omitted if the score is high (e.g., 8-10) and no significant improvements are needed."
                    "Suggestions can also include simplifying or removing features, not just adding them."
    )

class BalanceEvaluation(BaseMetricEvaluation):
    """Specific evaluation of the game's balance."""
    pass

class CoherenceEvaluation(BaseMetricEvaluation):
    """Specific evaluation of the game's coherence."""
    pass

class ClarityEvaluation(BaseMetricEvaluation):
    """Specific evaluation of the clarity of the rules and cards."""
    pass

class OriginalityEvaluation(BaseMetricEvaluation):
    """Specific evaluation of the game's originality and creativity."""
    pass

class PlayabilityEvaluation(BaseMetricEvaluation):
    """Specific evaluation of the playability and fun factor."""
    pass

class FidelityEvaluation(BaseMetricEvaluation):
    """Specific evaluation of the game's fidelity to the user's request."""
    pass

class GameEvaluation(BaseModel):
    """
    The final model that consolidates the evaluations from all specialist agents.
    Generated by the EvaluationSynthesizerAgent.
    """
    overall_score: float = Field(..., ge=0, le=10, description="The overall score for the game, calculated as the average of the metric scores.")
    summary: str = Field(..., description="An executive summary of the analysis, highlighting the game's strengths and weaknesses.")

    balance: BalanceEvaluation
    coherence: CoherenceEvaluation
    clarity: ClarityEvaluation
    originality: OriginalityEvaluation
    playability: PlayabilityEvaluation
    fidelity: FidelityEvaluation