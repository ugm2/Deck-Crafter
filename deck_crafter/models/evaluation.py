from pydantic import BaseModel, Field
from typing import List, Optional

# Metric weights for weighted average calculation (higher = more important)
METRIC_WEIGHTS = {
    "playability": 2.0,       # Fun matters most
    "balance": 1.5,           # Broken games aren't fun
    "clarity": 1.2,           # Players need to understand rules
    "theme_alignment": 1.0,   # Coherence + Fidelity merged
    "innovation": 0.8,        # Nice to have, not critical
}


def calculate_weighted_score(scores: dict[str, float]) -> float:
    """Calculate weighted average of metric scores."""
    total_weight = sum(METRIC_WEIGHTS.values())
    weighted_sum = sum(
        METRIC_WEIGHTS[metric] * score
        for metric, score in scores.items()
        if metric in METRIC_WEIGHTS
    )
    return weighted_sum / total_weight


class ScoreAdjustment(BaseModel):
    """Result of the second-pass cross-metric review."""
    original_score: int = Field(..., ge=0, le=10, description="The original score from pass 1")
    adjusted_score: float = Field(..., ge=0, le=10, description="The adjusted score after cross-metric review (±0.5 max)")
    adjustment_reason: str = Field(..., description="Why the score was adjusted based on other metrics")
    influenced_by: List[str] = Field(default_factory=list, description="Which other metrics influenced this adjustment")


class BaseMetricEvaluation(BaseModel):
    """Base class for evaluating a specific metric."""
    score: int = Field(..., ge=0, le=10, description="The score assigned to this metric, from 0 to 10.")
    analysis: str = Field(..., description="A detailed qualitative analysis of the metric.")
    suggestions: Optional[List[str]] = Field(
        None,
        description="A list of concrete, actionable suggestions for improvement. Focus on the 1-3 most impactful changes."
                    "This field is optional and should be omitted if the score is high (e.g., 8-10) and no significant improvements are needed."
                    "Suggestions can also include simplifying or removing features, not just adding them."
    )
    # Two-pass evaluation fields (set after cross-metric review)
    adjusted_score: Optional[float] = Field(None, description="Score after cross-metric review (±0.5 from original)")
    adjustment_reason: Optional[str] = Field(None, description="Why the score was adjusted")

    @property
    def final_score(self) -> float:
        """Return the adjusted score if available, otherwise the original score."""
        return self.adjusted_score if self.adjusted_score is not None else float(self.score)


class BalanceEvaluation(BaseMetricEvaluation):
    """Specific evaluation of the game's balance."""
    pass


class ClarityEvaluation(BaseMetricEvaluation):
    """Specific evaluation of the clarity of the rules and cards."""
    pass


class PlayabilityEvaluation(BaseMetricEvaluation):
    """Specific evaluation of the playability and fun factor."""
    pass


class ThemeAlignmentEvaluation(BaseMetricEvaluation):
    """
    Evaluation of how well the game fulfills its intended vision.
    Merges two aspects:
    - Coherence: Do mechanics match the theme?
    - Fidelity: Does the game match the user's request?
    """
    pass


class InnovationEvaluation(BaseMetricEvaluation):
    """Specific evaluation of the game's originality and creativity."""
    pass


# Legacy aliases for backward compatibility
CoherenceEvaluation = ThemeAlignmentEvaluation
FidelityEvaluation = ThemeAlignmentEvaluation
OriginalityEvaluation = InnovationEvaluation


class ModelScore(BaseModel):
    """Per-model score breakdown from panel evaluation."""
    model_id: str
    scores: dict[str, float]  # metric_name -> final_score
    overall_score: float


class GameEvaluation(BaseModel):
    """
    The final model that consolidates the evaluations from all specialist agents.
    Generated by the EvaluationSynthesizerAgent.
    Uses 5 metrics with weighted scoring and two-pass evaluation.
    """
    overall_score: float = Field(..., ge=0, le=10, description="Weighted average of metric scores.")
    summary: str = Field(..., description="An executive summary highlighting strengths and weaknesses.")

    balance: BalanceEvaluation
    clarity: ClarityEvaluation
    playability: PlayabilityEvaluation
    theme_alignment: ThemeAlignmentEvaluation
    innovation: InnovationEvaluation

    # Synthesized suggestions (deduplicated, prioritized)
    synthesized_suggestions: Optional["SynthesizedSuggestions"] = None

    # Multi-model panel scores (set when panel evaluation is used)
    model_scores: Optional[List[ModelScore]] = None

    def get_scores_dict(self) -> dict[str, float]:
        """Return all metric scores as a dictionary (using adjusted scores if available)."""
        return {
            "balance": self.balance.final_score,
            "clarity": self.clarity.final_score,
            "playability": self.playability.final_score,
            "theme_alignment": self.theme_alignment.final_score,
            "innovation": self.innovation.final_score,
        }

class EvaluationSummary(BaseModel):
    """Simple model for the synthesizer to generate only the executive summary."""
    summary: str = Field(..., description="An executive summary of the analysis, highlighting the game's strengths and weaknesses.")


class ValidationResult(BaseModel):
    """Representa el resultado de una validación interna durante la generación."""
    is_valid: bool = Field(..., description="True si la salida es aceptable, False si necesita revisión.")
    feedback: str = Field(..., description="El feedback detallado. Si es válido, responde 'OK'. Si no, explica qué corregir.")


# --- Suggestion Synthesis Models ---

class PrioritizedSuggestion(BaseModel):
    """A single prioritized suggestion from synthesis."""
    suggestion: str = Field(..., description="The actionable suggestion text")
    target: str = Field(..., description="What this affects: 'rules', 'cards', or 'both'")
    target_card: Optional[str] = Field(None, description="Specific card name if card-related")
    estimated_impact: float = Field(..., ge=0, le=2.0, description="Expected score improvement (0-2.0)")
    priority: str = Field(..., description="Priority level: 'high', 'medium', or 'low'")
    source_metrics: List[str] = Field(default_factory=list, description="Which metrics suggested this")
    conflicts_with: List[str] = Field(default_factory=list, description="Other suggestions that conflict")


class SynthesizedSuggestions(BaseModel):
    """Result of suggestion synthesis and deduplication."""
    high_priority: List[PrioritizedSuggestion] = Field(default_factory=list, description="Do these first")
    medium_priority: List[PrioritizedSuggestion] = Field(default_factory=list, description="Do these second")
    low_priority: List[PrioritizedSuggestion] = Field(default_factory=list, description="Optional improvements")
    conflicts: List[dict] = Field(default_factory=list, description="Pairs of suggestions that contradict")
    total_suggestions: int = Field(0, description="Original number of suggestions before dedup")
    deduplicated_count: int = Field(0, description="Number after deduplication")